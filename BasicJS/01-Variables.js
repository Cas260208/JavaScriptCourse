// var: la forma original para declarar variables
// Al igual no es la forma correcta 
var helloWorld = "Hello World";
console.log(helloWorld); 

helloWorld = "Hello JavaScript!";
console.log(helloWorld);

// Estas ya son las variables que se usan actualmente 
// let
let helloWorld2 = "Hello JavaScript";
console.log(helloWorld2);

helloWorld2 = "Hello World!";
console.log(helloWorld2);

// const: Esta varibale no puede ser reasignada; ya que, es una constante 
// const también es más utilizado para arrays 
const helloWorld3 = "Hello JavaScript";
console.log(helloWorld3);
